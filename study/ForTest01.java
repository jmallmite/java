////////////////
// ForTest01 클래스 정의
// 프로그램 시작
// int 에 i 변수를 정의
// i가 0부터 10보다 작거나 같을때 까지 i에 1씩 증가 하는 반복문 -> i는 0부터 10까지 총 11번을 반복하게 된다.
///////////////


public class ForTest01 {
	
	public static void main(String[] args) {
		int i;
		for (i = 0; i <= 10; i++) {
			System.out.print("+");
		}
		
		
		System.out.println("---------------------");

		
		for (i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		
	
	}
}
